def addition(num1, num2)
  x = num1 + num2
  return x
end
  
def subtraction(num1, num2)
  x = num1 - num2
  return x
end
  
def division(num1, num2)
  x = num1 / num2
  return x
end
  
def multiplication(num1, num2)
  x = num1 * num2
  return x
end
  
def modulo(num1, num2)
  x = num1 % num2
  return x
end
  
def square_root(num)
  x = Math.sqrt(num)
  return x
end